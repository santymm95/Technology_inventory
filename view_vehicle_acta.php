        <h3>CLÁUSULAS ADICIONALES</h3>
        <p>
            "El Recibidor" se compromete a respetar los límites de velocidad establecidos por la legislación colombiana, teniendo presente que:<br>
            En vías urbanas y municipales, la velocidad máxima permitida es de 50 kph.<br>
            En zonas escolares y residenciales, se debe mantener una velocidad de hasta 30 kph.<br>
            En vías rurales, el límite de velocidad autorizado es de 80 kph.<br>
            En carreteras nacionales y departamentales, se establece una velocidad máxima de 90 kph.<br><br>

            "El Recibidor” se compromete a informar de forma veraz su estado de salud.<br>
            "El Recibidor” se compromete a respetar las normas de tránsito.<br>
            "El Recibidor” se compromete a informar al dueño del vehículo, jefe inmediato y/o auxiliar de SST cualquier anomalía, incidente o accidente de tránsito que presente el vehículo por insignificante que parezca.<br>
            "El Recibidor” se compromete a portar los documentos tanto del vehículo como los personales ordenados por la autoridad competente.<br>
            "El Recibidor” se compromete a mantener el vehículo en óptimas condiciones de funcionamiento y limpieza.<br>
            "El Recibidor” se compromete a cuidar y preservar el estado general del vehículo.<br>
            "El Recibidor” se compromete a usar el cinturón de seguridad y exigir su uso a los ocupantes del vehículo que conduzca.<br>
            "El Recibidor” se compromete a usar los dispositivos de comunicaciones solo cuando se encuentre detenido en lugar seguro y habilitado para estacionamiento, evitando que puedan causarle distracción y por consiguiente probables accidentes de tránsito.<br>
            "El Recibidor” se compromete a usar los elementos de protección personal durante la conducción de vehículos en cumplimiento de la misión de la empresa.<br>
            "El Recibidor” se compromete a asistir a las capacitaciones, charlas y eventos citados por la organización.<br>
            "El Recibidor” se compromete a estar a paz y salvo con el pago de comparendos por infracciones de tránsito.<br>
            "El Recibidor” se compromete a guardar el vehículo en un lugar seguro.<br><br>

            <b>CLAUSULA TERCERA: RESPONSABILIDADES DE LA EMPRESA:</b><br>
            ACEMA INGENIERÍA SAS se compromete a entregar el vehículo en óptimas condiciones de funcionamiento.<br>
            ACEMA INGENIERÍA SAS proporcionará el soporte técnico necesario para el mantenimiento preventivo o correctivo del vehículo, de acuerdo con los plazos establecidos y las necesidades que se presenten.<br>
            ACEMA INGENIERÍA SAS llevará un registro detallado de las fechas de mantenimiento del vehículo, incluyendo cualquier reparación o servicio realizado.<br>
            ACEMA INGENIERÍA SAS proporcionará a "El Recibidor" la información necesaria para realizar el seguimiento y mantenimiento adecuado del vehículo.<br><br>

            <b>CLAUSULA CUARTA: HURTO, ACCIDENTE O DAÑOS</b><br>
            En caso de presentarse hurto del vehículo se deberá instaurar de inmediato la denuncia ante la policía nacional y relatar los hechos e informar por correo electrónico a mariana.agudelo@acemaingenieria.com Gerente Administrativa, y gerencia@acemaingenieria.com Gerente General, adjuntando el denuncio.<br>
            En caso de accidente o daño al vehículo, el empleado debe informar a la empresa y a las autoridades competentes de inmediato. El conductor deberá presentar un informe detallado del incidente y enviarlo por correo electrónico a mariana.agudelo@acemaingenieria.com Gerente Administrativa, y gerencia@acemaingenieria.com Gerente General. Si el daño es producto de negligencia o mal uso del vehículo, los costos asociados son deducidos de nómina.<br><br>

            <b>CLAUSULA QUINTA: MULTAS</b><br>
            El trabajador se compromete a asumir la responsabilidad por cualquier infracción de tránsito que cometa durante el período de arrendamiento del vehículo o cuando utilice vehículos de la empresa, incluyendo, multas por exceso de velocidad, estacionamiento indebido, uso incorrecto de los documentos del vehículo, foto multas etc. En caso de que el colaborador reciba una multa, deberá abonar dicha multa de forma inmediata, así como los gastos administrativos generados por el proceso igualmente.<br>
            En caso de que la empresa pague una multa en nombre del trabajador, este se compromete a reembolsar el monto total de la multa a partir de la notificación formal de la misma, además de los posibles gastos administrativos generados. En caso de que el trabajador no cumpla con el reembolso dentro del plazo establecido, la empresa podrá imponer sanciones adicionales, que podrán incluir la retención de salarios o la terminación del contrato de trabajo, dependiendo de la gravedad de la infracción cometida.<br><br>

            <b>CLAUSULA SÉPTIMA: CAMBIOS</b><br>
            NO realizar cambios de carrocería, color, rines, cámara, u cualquier otro elemento sin previa autorización del jefe inmediato.<br><br>

            <b>CLAUSULA OCTAVA: PROHIBICIONES</b><br>
            NO TRANSPORTAR personal ajeno a la compañía, a menos de que este sea autorizado por su jefe inmediato.<br>
            NO PRESTAR el vehículo a ninguna persona, cuando el directo responsable es Usted.<br>
            NO consumir, portar o distribuir bebidas alcohólicas o sustancias psicoactivas en los sitios de trabajo, instalaciones, vehículos o áreas relacionadas con las operaciones de la empresa.<br>
            NO USAR teléfonos móviles ni dispositivos electrónicos mientras se conduce, salvo cuando se utilicen sistemas de manos libres y sin distraer la atención del conductor.<br>
            Queda expresamente prohibido el uso del vehículo para el transporte de bienes no relacionados con las actividades de la empresa, así como para fines personales, recreativos o cualquier otro propósito ajeno a las necesidades laborales autorizadas.<br><br>

            <b>CLAUSULA NOVENA: VIGENCIA</b><br>
            La presente acta de entrega tiene vigencia a partir de la fecha de entrega y se mantendrá en vigor hasta que ACEMA INGENIERÍA SAS considere que se ha cumplido con las condiciones establecidas o hasta que se acuerde la devolución o retiro del mismo.<br><br>

